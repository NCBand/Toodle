package ru.ncband.web.shared.classes;

public class Lesson {
    private String id = null;
    private String name = null;

    public Lesson() {}

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
